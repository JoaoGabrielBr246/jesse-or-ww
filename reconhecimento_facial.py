from ultralytics import YOLO
import tensorflow as tf
import numpy as np
import cv2
import matplotlib.pyplot as plt
import matplotlib.image as mpimg
from tensorflow.keras.preprocessing import image

# Carregar o modelo YOLO para detecção de rosto
yolo_model = YOLO('yolov8n.pt')

# Carregar o modelo de classificação
classifier_model = tf.keras.models.load_model('walter_jesse_model.h5')

# Dimensões para o redimensionamento (conforme o modelo)
IMG_HEIGHT = 180
IMG_WIDTH = 180

# Função para preprocessar o rosto
def preprocess_face(face_img):
    face_img = cv2.resize(face_img, (IMG_HEIGHT, IMG_WIDTH))  # Redimensionar
    face_img = np.expand_dims(face_img, axis=0) / 255.0  # Normalizar
    return face_img

# Caminho para a imagem de teste
test_image_path = '/content/drive/MyDrive/Colab Notebooks/teste3.jpg'

# Realizar a detecção de rostos na imagem
results = yolo_model(test_image_path)

# Carregar a imagem original
image = cv2.imread(test_image_path)

# Processar cada rosto detectado com confiança acima de um limite
confidence_threshold = 0.5  # Ajuste para filtrar apenas rostos de alta confiança

for box in results[0].boxes:
    if box.conf[0] < confidence_threshold:
        continue  # Ignorar detecções com baixa confiança

    x1, y1, x2, y2 = map(int, box.xyxy[0])  # Coordenadas do rosto detectado

    # Extrair a região do rosto da imagem
    face = image[y1:y2, x1:x2]

    # Preprocessar o rosto para o modelo de classificação
    preprocessed_face = preprocess_face(face)

    # Realizar a predição para identificar o personagem
    predictions = classifier_model.predict(preprocessed_face)
    label = "Walter White" if predictions[0] > 0.5 else "Jesse Pinkman"

    # Adicionar retângulo e rótulo ao redor do rosto
    color = (0, 255, 0) if label == "Walter White" else (255, 0, 0)
    cv2.rectangle(image, (x1, y1), (x2, y2), color, 2)
    cv2.putText(image, label, (x1, y1 - 10), cv2.FONT_HERSHEY_SIMPLEX, 0.5, color, 2)

# Salvar e exibir a imagem resultante
output_image_path = '/content/image_with_names.jpg'
cv2.imwrite(output_image_path, image)

# Exibir a imagem completa com os rótulos aplicados apenas aos rostos
image_with_names = mpimg.imread(output_image_path)
plt.imshow(image_with_names)
plt.axis('off')
plt.show()